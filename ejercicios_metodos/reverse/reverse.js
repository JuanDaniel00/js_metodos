// reverse( )
// Este método invierte una matriz en su lugar. El elemento en el último índice será el primero y el 
// elemento en el índice 0 será el último.

// Pepita necesita invertir una lista de alumnos

const alumnos = ["Pepita", "Pepito", "Pepitu"];
alumnos.reverse();
console.log(alumnos);

